def detect(text: str):
    """
    Detect common error patterns in response text.
    
    Only returns an error if a known error pattern is found.
    Returns None for normal/valid responses.
    """
    if not text:
        return None
    
    # Check for known error patterns only
    for flag, err in COMMON_ERRORS.items():
        if flag in text:
            return err
    
    # No error pattern found - this is a normal response
    return None


def solution_of(err_type: str) -> str:
    return ERRORS_TYPES.get(err_type, "")


# -------------------------
# THRESHOLD LOGIC (FIX FOR is_important ERROR)
# -------------------------

THRESHOLD = 3  # default %

ERROR_THRESHOLDS: Dict[str, float] = {
    "Connecting failure (DNS)": 10,
}


def threshold_for(err_type: str) -> float:
    return ERROR_THRESHOLDS.get(err_type, THRESHOLD)


def is_important(err_data: Dict[str, Any]) -> bool:
    """
    Decide if an error type is significant enough to show in notifications.
    """
    if not err_data:
        return False

    return err_data.get("perc", 0) >= threshold_for(err_data.get("err", ""))


def is_permanent(err_type: str) -> bool:
    return err_type not in [
        "Request timeout",
        "Unknown",
        "Request failed",
        "Connecting failure",
        "Connecting failure (DNS)",
        "Webgate unavailable",
        "HTTP",
        "Proxy",
        "Interrupted",
        "Connection lost",
    ]


