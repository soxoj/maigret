class CheckError:
    _type = "Unknown"
    _desc = ""
    _context = None

    def __init__(self, typename, desc="", context=None):
        self._type = typename
        self._desc = desc
        self._context = context  # raw failure context

    def __str__(self):
        base = f"{self._type} error"
        if self._desc:
            base += f": {self._desc}"
        if self._context:
            base += f" | {self._context[:120]}"
        return base

    @property
    def type(self):
        return self._type

    @property
    def desc(self):
        return self._desc

    @property
    def context(self):
        return self._context


